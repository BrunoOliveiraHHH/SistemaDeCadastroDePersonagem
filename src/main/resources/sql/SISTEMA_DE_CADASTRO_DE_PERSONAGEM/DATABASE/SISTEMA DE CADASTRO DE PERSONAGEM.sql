CREATE DATABASE SISTEMA_DE_CADASTRO_DE_PERSONAGEM;

USE SISTEMA_DE_CADASTRO_DE_PERSONAGEM;

CREATE TABLE JOGADOR (
    ID_JOGADOR INT AUTO_INCREMENT,
    NOME VARCHAR(255) NOT NULL,
    NUMERO_TELEFONE VARCHAR(15),
    PRIMARY KEY (ID_JOGADOR)
);

CREATE TABLE STATUS_PERSONAGEM (
    ID_STATUS_PERSONAGEM INT AUTO_INCREMENT,
    PONTOS_VIDA_MAXIMA INT,
    PONTOS_VIDA_ATUAL INT,
    PONTOS_VIDA_TEMPORARIA INT,
    CLASSE_ARMADURA INT,
    BONUS_PROFICIENCIA INT,
    PERCEPCAO_PASSIVA INT,
    DESLOCAMENTO VARCHAR(255),
    DADOS_DE_VIDA INT,
    STATUS_MORTE INT,
    TENDENCIA VARCHAR(255),
    PRIMARY KEY (ID_STATUS_PERSONAGEM)
);

CREATE TABLE PARAMETRO_HABILIDADE (
    ID_PARAMETRO_HABILIDADE INT AUTO_INCREMENT,
    NOME_HABILIDADE VARCHAR(255),
    PRIMARY KEY (ID_PARAMETRO_HABILIDADE)
);

CREATE TABLE PARAMETRO_CLASSE (
    ID_PARAMETRO_CLASSE INT AUTO_INCREMENT,
    NOME_CLASSE VARCHAR(255),
    DADO_DE_VIDA INT, 
    PRIMARY KEY (ID_PARAMETRO_CLASSE)
);

CREATE TABLE PARAMETRO_PROFICIENCIA (
    ID_PROFICIENCIA INT AUTO_INCREMENT, 
    NOME_PROFICIENCIA VARCHAR(255),
    TIPO_PROFICIENCIA INT, -- 1 Arma, 2 Armadura, 3 Idioma e 4 Ferramenta
    PRIMARY KEY (ID_PROFICIENCIA)
);

CREATE TABLE PARAMETRO_RACA (
    ID_RACA INT AUTO_INCREMENT,
    NOME_RACA VARCHAR(255),
    PRIMARY KEY (ID_RACA)
);

CREATE TABLE PARAMETRO_SUB_RACA (
ID_SUB_RACA INT AUTO_INCREMENT,
NOME_SUB_RACA VARCHAR (255),
ID_RACA INT,
PRIMARY KEY (ID_SUB_RACA)
);

ALTER TABLE PARAMETRO_SUB_RACA
ADD CONSTRAINT FK_RACA_SUBRACA FOREIGN KEY (ID_RACA) REFERENCES PARAMETRO_RACA(ID_RACA);


CREATE TABLE CLASSE_SALVAGUARDA (
    ID_CLASSE_SALVAGUARDA INT AUTO_INCREMENT,
    ID_CLASSE INT,
    ID_HABILIDADE INT, 
    PRIMARY KEY (ID_CLASSE_SALVAGUARDA)
);

ALTER TABLE CLASSE_SALVAGUARDA
ADD CONSTRAINT FK_CLASSE_SALVAGUARDA FOREIGN KEY (ID_CLASSE) REFERENCES PARAMETRO_CLASSE (ID_PARAMETRO_CLASSE);

ALTER TABLE CLASSE_SALVAGUARDA
ADD CONSTRAINT FK_HABILIDADE_SALVAGUARDA FOREIGN KEY (ID_HABILIDADE) REFERENCES PARAMETRO_HABILIDADE (ID_PARAMETRO_HABILIDADE);


CREATE TABLE PARAMETRO_TRACOS_RACIAIS (
    ID_TRACOS_RACIAIS INT AUTO_INCREMENT,
    NOME_TRACOS_RACIAIS VARCHAR(255),
    CARACTERISTICAS_RACIAIS TEXT,
    PRIMARY KEY (ID_TRACOS_RACIAIS)
);

CREATE TABLE RACA_TRACOS_RACIAIS (
    ID_RACA_TRACOS_RACIAIS INT AUTO_INCREMENT,
    ID_RACA INT,
    ID_TRACOS_RACIAIS INT,
    PRIMARY KEY (ID_RACA_TRACOS_RACIAIS)
   
);

ALTER TABLE RACA_TRACOS_RACIAIS
ADD CONSTRAINT FK_TRACOS_RACIAIS_01  FOREIGN KEY (ID_RACA) REFERENCES PARAMETRO_RACA(ID_RACA);

ALTER TABLE RACA_TRACOS_RACIAIS
ADD CONSTRAINT FK_ID_TRACOS FOREIGN KEY (ID_TRACOS_RACIAIS) REFERENCES PARAMETRO_TRACOS_RACIAIS(ID_TRACOS_RACIAIS);


CREATE TABLE PARAMETRO_PERICIA (
    ID_PARAMETRO_PERICIA INT AUTO_INCREMENT,
    NOME_PERICIA VARCHAR(255),
    ID_PARAMETRO_HABILIDADE INT,
    PRIMARY KEY (ID_PARAMETRO_PERICIA)
  
);

ALTER TABLE PARAMETRO_PERICIA
ADD CONSTRAINT FK_HABILIDADE_PERICIAS   FOREIGN KEY (ID_PARAMETRO_HABILIDADE) REFERENCES PARAMETRO_HABILIDADE(ID_PARAMETRO_HABILIDADE);


CREATE TABLE HABILIDADES (
    ID_HABILIDADE INT AUTO_INCREMENT,
    ID_PARAMETRO_HABILIDADE INT,
    VALOR_HABILIDADE INT,
    MODIFICADOR_HABILIDADE INT,
    POSSUI_SALVAGUARDA INT,
    PRIMARY KEY (ID_HABILIDADE)
  
);

ALTER TABLE HABILIDADES
ADD CONSTRAINT FK_HABILIDADES01   FOREIGN KEY (ID_PARAMETRO_HABILIDADE) REFERENCES PARAMETRO_HABILIDADE(ID_PARAMETRO_HABILIDADE);

CREATE TABLE PERICIAS (
    ID_PERICIA INT AUTO_INCREMENT,
    ID_PARAMETRO_PERICIA INT,
    VALOR_PERICIA INT,
    POSSUI_PERICIA INT,
    PRIMARY KEY (ID_PERICIA)
    
);

ALTER TABLE PERICIAS 
ADD CONSTRAINT FK_PERICIAS01 FOREIGN KEY (ID_PARAMETRO_PERICIA) REFERENCES PARAMETRO_PERICIA(ID_PARAMETRO_PERICIA);


CREATE TABLE PERSONAGEM (
    ID_PERSONAGEM INT AUTO_INCREMENT,
    NOME_PERSONAGEM VARCHAR(255),
    ID_RACA INT, 
    ID_SUB_RACA INT,
    ID_PARAMETRO_CLASSE INT,
    ANTECEDENTE VARCHAR(255), 
    NIVEL INT NOT NULL CHECK (NIVEL BETWEEN 1 AND 30),
    XP INT,
    TIPO_CONJURADOR INT, 
    PRIMARY KEY (ID_PERSONAGEM)
);

ALTER TABLE PERSONAGEM
ADD CONSTRAINT FK_01_ID_RACA FOREIGN KEY (ID_RACA) REFERENCES PARAMETRO_RACA(ID_RACA);

ALTER TABLE PERSONAGEM 
ADD CONSTRAINT FK_01_ID_SUB_RACA FOREIGN KEY (ID_SUB_RACA) REFERENCES PARAMETRO_SUB_RACA (ID_SUB_RACA);

ALTER TABLE PERSONAGEM 
ADD CONSTRAINT FK_01_ID_PARAMETRO_CLASSE FOREIGN KEY (ID_PARAMETRO_CLASSE) REFERENCES PARAMETRO_CLASSE(ID_PARAMETRO_CLASSE); 


CREATE TABLE FICHA_PERSONAGEM (
    ID_FICHA_PERSONAGEM INT AUTO_INCREMENT,
    ID_PERSONAGEM INT,
    ID_JOGADOR INT,
    ID_HABILIDADE INT,
    ID_PERICIA INT,
    ID_STATUS_PERSONAGEM INT,
    PRIMARY KEY (ID_FICHA_PERSONAGEM)
    
);

ALTER  TABLE FICHA_PERSONAGEM
ADD CONSTRAINT FK_01_ID_PERSONAGEM FOREIGN KEY (ID_PERSONAGEM) REFERENCES PERSONAGEM(ID_PERSONAGEM);

ALTER TABLE FICHA_PERSONAGEM
ADD CONSTRAINT FK_02_ID_JOGADOR  FOREIGN KEY (ID_JOGADOR) REFERENCES JOGADOR(ID_JOGADOR);

ALTER TABLE FICHA_PERSONAGEM
ADD CONSTRAINT FK_03_HABILIDDADE_PERSONAGEM  FOREIGN KEY (ID_HABILIDADE) REFERENCES HABILIDADES(ID_HABILIDADE);

ALTER TABLE FICHA_PERSONAGEM
ADD CONSTRAINT FK_04_PERSONAGEM_PERICIAS   FOREIGN KEY (ID_PERICIA) REFERENCES PERICIAS(ID_PERICIA);

ALTER TABLE FICHA_PERSONAGEM
ADD CONSTRAINT FK_05_STATUS_PERSONAGEM  FOREIGN KEY (ID_STATUS_PERSONAGEM) REFERENCES STATUS_PERSONAGEM(ID_STATUS_PERSONAGEM);



